# Decodificar msg encriptado
# A = Brasil
# B = Argentina
# C = Bolivia
# D = Peru
import os
# input
msg = os.sys.argv [ 1 ].upper ()
# bucle
for letra in msg:
    if letra == "A":
        print("Brasil")
    if letra ==  "B":
        print("Argentina")
    if letra ==  "C":
        print("Bolivia")
    if letra ==  "D":
        print("Peru")
# fin_iterador

print ( " Fin del bucle ")
